    public static Root fill(JSONObject jsonobj){
        Root entity = new Root();
        if (jsonobj.containsKey("confirmed")) {
            entity.setConfirmed(jsonobj.getJSONArray("confirmed"));        
        }
        if (jsonobj.containsKey("dt")) {
            entity.setDt(jsonobj.getString("dt"));        
        }
        if (jsonobj.containsKey("ts")) {
            entity.setTs(jsonobj.getInt("ts"));        
        }
        return entity;
    }
    public static List<Root> fillList(JSONArray jsonarray) {
        if (jsonarray == null || jsonarray.size() == 0)
            return null;
        List<Root> olist = new ArrayList<Root>();
        for (int i = 0; i < jsonarray.size(); i++) {
            olist.add(fill(jsonarray.getJSONObject(i)));
        }
        return olist;
    }
